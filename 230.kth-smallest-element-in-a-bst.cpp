#include <stack>
using namespace std;

class Solution {
public:
  int kthSmallest(TreeNode *root, int k) {
    stack<TreeNode *> st;
    auto cur = root;
    for (; cur != nullptr || !st.empty();) {
      if (cur == nullptr) {
        cur = st.top();
        st.pop();
        if (--k == 0) {
          return cur->val;
        }
        cur = cur->right;
        continue;
      }
      st.push(cur);
      cur = cur->left;
    }
    return -1;
  }
};
